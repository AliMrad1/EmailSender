    public void addNewEmail(Email email) {

        boolean isValid = emailValidator.test(email.getEmail());
        if(isValid) {
            Optional<Email> optionalStudent = emailRepository.findSameEmail(email.getEmail());
            if (optionalStudent.isPresent()) {
                throw new IllegalStateException("Email Taken");
            }

            emailRepository.save(email);
        }
        else {
            throw new IllegalStateException("email not matched");
        }

}

